import 'package:flutter/foundation.dart';

enum CountryField {
  FLAG,
  DIAL,
  NAME,
  CODE,
}

class Country {
  const Country({
    @required this.code,
    @required this.dial,
    @required this.flag,
    @required this.name,
  })  : assert(code != null),
        assert(dial != null),
        assert(flag != null),
        assert(name != null);

  final String code;
  final String dial;
  final String flag;
  final String name;

  String getField(CountryField field) {
    switch (field) {
      case CountryField.CODE:
        return code;
      case CountryField.DIAL:
        return dial;
      case CountryField.FLAG:
        return flag;
      case CountryField.NAME:
        return name;
      default:
        return '';
    }
  }
}

